#include <cassert>
#include <functional> // std::ref
#include <iostream>
#include <thread>

// ==============================================================================
// [요구사항 1] 데이터 증가 함수 작성
// 역할: 전달받은 정수 데이터를 1 증가시킵니다.
// 주의: "함수 호출이 끝난 후에도 외부에 있는 원본 데이터가 수정된 결과를 온전히
// 보존해야 합니다." (함수 이름, 매개변수 타입, 반환 타입은 직접 결정하여
// 작성하세요.)
// ==============================================================================

// 여기에 함수를 작성하세요.
void Increment(int &a)
{
    a++;
}

// ==============================================================================
// [테스트 영역] 아래의 빈칸을 채워 테스트를 통과시키세요.
// ==============================================================================
void TestThreadBasic()
{
    int count = 0;

    // [요구사항 2] 스레드 생성
    // 위에서 작성하신 함수를 실행하는 별도의 스레드 't1'을 생성하세요.
    // 힌트: 내부에서 값을 수정해야 하므로 std::ref(count)를 통해 전달해야
    // 합니다!

    // 여기에 스레드 생성 코드 작성
    auto ptr = std::ref(count);
    std::thread t1(Increment, ptr);

    // [요구사항 3] 안전한 종료
    // 메인 흐름이 스레드의 작업이 полностью 끝날 때까지 기다리도록 설정하세요.

    // 여기에 스레드 종료 대기 코드 작성
    t1.join();

    // 검증 로직
    assert(count == 1 && "스레드가 원본 변수를 1 증가시키지 못했습니다.");
    std::cout << "[PASS] 스레드 생성 및 공유 변수 제어 성공!\n";
}

int main()
{
    std::cout << "--- Module 11: Thread Basics Practice ---\n";
    TestThreadBasic();
    return 0;
}
